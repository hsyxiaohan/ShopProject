@Entity
public class Goods {

    @PrimaryKey(autoGenerate = true)
    private Long id;

    //商品图片
    private String pic;

    //商品名称
    private String text;

    //商品价格
    private String price;

    //商品数量
    private String count;

    @Ignore
    private boolean ischecked = false;

    public Goods(Long id, String pic, String text, String price, String count, boolean ischecked) {
        this.id = id;
        this.pic = pic;
        this.text = text;
        this.price = price;
        this.count = count;
        this.ischecked = ischecked;
    }



    public Goods(Long id, String pic, String text, String price, String count) {
        this.id = id;
        this.pic = pic;
        this.text = text;
        this.price = price;
        this.count = count;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public boolean isIschecked() {
        return ischecked;
    }

    public void setIschecked(boolean ischecked) {
        this.ischecked = ischecked;
    }
}
